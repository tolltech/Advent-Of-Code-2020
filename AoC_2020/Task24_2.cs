        public long Task(string input, int movesCount)
        {
            var lines = input.Split(new[] {"\r", "\n"}, StringSplitOptions.RemoveEmptyEntries).ToArray();

            var blackTiles = new HashSet<(int E, int Ne, int Nw)>();

            foreach (var line in lines)
            {
                var commands = line.Replace("se", "se,").Replace("sw", "sw,").Replace("nw", "nw,").Replace("ne", "ne,")
                    .Replace("w", "w,").Replace("e", "e,")
                    .Split(",", StringSplitOptions.RemoveEmptyEntries);

                (int E, int Ne, int Nw) tile = default;

                foreach (var command in commands)
                {
                    (int E, int Ne, int Nw) step = default;
                    switch (command)
                    {
                        case "ne":
                            step = (E: 1, Ne: 0, Nw: 1);
                            break;
                        case "e":
                            step = (E: 0, Ne: -1, Nw: 1);
                            break;
                        case "se":
                            step = (E: -1, Ne: -1, Nw: 0);
                            break;
                        case "sw":
                            step = (E: -1, Ne: 0, Nw: -1);
                            break;
                        case "w":
                            step = (E: 0, Ne: 1, Nw: -1);
                            break;
                        case "nw":
                            step = (E: 1, Ne: 1, Nw: 0);
                            break;
                        default: throw new Exception("tile");
                    }

                    tile.Nw += step.Nw;
                    tile.Ne += step.Ne;
                    tile.E += step.E;
                }

                if (blackTiles.Contains(tile))
                {
                    blackTiles.Remove(tile);
                }
                else
                {
                    blackTiles.Add(tile);
                }
            }



            return blackTiles.Count;
        }

